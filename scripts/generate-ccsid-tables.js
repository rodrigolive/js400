#!/usr/bin/env node
/**
 * CCSID table code generator.
 *
 * Reads ConvTable*.java source files from JTOpen and generates
 * compact JS modules for runtime CCSID conversion.
 *
 * No Java runtime required. Reads .java sources directly.
 *
 * Usage: node scripts/generate-ccsid-tables.js [jtopen-dir]
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync } from 'node:fs';
import { join, basename } from 'node:path';

const JTOPEN_DIR = process.argv[2] || join(process.cwd(), 'JTOpen', 'src', 'main', 'java', 'com', 'ibm', 'as400', 'access');
const OUT_DIR = join(process.cwd(), 'src', 'ccsid', 'generated');

// Tier classifications
const TIER1 = new Set([37,256,273,277,278,280,284,285,290,297,420,423,424,500,870,871,875,880,905,1025,1026,1027,1112,1122,1123,1130,1140,1141,1142,1143,1144,1145,1146,1147,1148,1149,1153,1154,1155,1156,1157,1158,1160,1164]);
const TIER2_SINGLE = new Set([]);
const TIER2_SPECIAL = new Set([1200, 1208, 13488, 61952, 65535]);
const TIER2_MIXED = new Set([930, 933, 935, 937, 939, 1364, 1388, 1399, 5026, 5035]);

function parseJavaUnicodeString(javaSource, varName) {
  const escaped = new RegExp(`(private\\s+)?static\\s+(final\\s+)?String\\s+${varName}\\s*=`, 'm');
  const match = javaSource.match(escaped);
  if (!match) return null;

  let pos = match.index + match[0].length;
  let result = '';
  let inString = false;
  let depth = 0;

  while (pos < javaSource.length) {
    const ch = javaSource[pos];

    if (ch === ';' && !inString && depth === 0) break;

    if (ch === '"' && (pos === 0 || javaSource[pos - 1] !== '\\')) {
      inString = !inString;
      pos++;
      continue;
    }

    if (inString) {
      if (ch === '\\' && pos + 1 < javaSource.length) {
        const next = javaSource[pos + 1];
        if (next === 'u') {
          const hex = javaSource.substring(pos + 2, pos + 6);
          result += String.fromCharCode(parseInt(hex, 16));
          pos += 6;
          continue;
        } else if (next === 'n') {
          result += '\n';
          pos += 2;
          continue;
        } else if (next === 'r') {
          result += '\r';
          pos += 2;
          continue;
        } else if (next === 't') {
          result += '\t';
          pos += 2;
          continue;
        } else if (next === 'f') {
          result += '\f';
          pos += 2;
          continue;
        } else if (next === 'b') {
          result += '\b';
          pos += 2;
          continue;
        } else if (next === '\\') {
          result += '\\';
          pos += 2;
          continue;
        } else if (next === '"') {
          result += '"';
          pos += 2;
          continue;
        } else if (next === "'") {
          result += "'";
          pos += 2;
          continue;
        }
      }
      result += ch;
    }
    pos++;
  }

  return result;
}

function detectTableType(javaSource) {
  if (/extends\s+ConvTableSingleMap/.test(javaSource)) return 'single';
  if (/extends\s+ConvTableDoubleMap/.test(javaSource)) return 'double';
  if (/extends\s+ConvTableMixedMap/.test(javaSource)) return 'mixed';
  if (/extends\s+ConvTableBidiMap/.test(javaSource)) return 'single';
  if (/extends\s+ConvTableAsciiMap/.test(javaSource)) return 'single';
  return null;
}

function extractMixedSubCcsids(javaSource) {
  const match = javaSource.match(/super\s*\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\)/);
  if (match) {
    return { ccsid: parseInt(match[1]), sbCcsid: parseInt(match[2]), dbCcsid: parseInt(match[3]) };
  }
  return null;
}

function charArrayToU16Hex(str) {
  let hex = '';
  for (let i = 0; i < str.length; i++) {
    hex += str.charCodeAt(i).toString(16).padStart(4, '0');
  }
  return hex;
}

function generateSingleByteModule(ccsid, toUnicode, fromUnicode) {
  const toU = charArrayToU16Hex(toUnicode);
  const fromU = fromUnicode ? charArrayToU16Hex(fromUnicode) : '';

  return `// Generated CCSID ${ccsid} - Single-byte EBCDIC table
// Do not edit manually. Generated by scripts/generate-ccsid-tables.js
export const ccsid = ${ccsid};
export const type = 'single';
export const toUnicode = '${toU}';
export const fromUnicode = '${fromU}';
`;
}

function generateMixedModule(ccsid, sbCcsid, dbCcsid) {
  return `// Generated CCSID ${ccsid} - Mixed-byte table
// Do not edit manually. Generated by scripts/generate-ccsid-tables.js
export const ccsid = ${ccsid};
export const type = 'mixed';
export const sbCcsid = ${sbCcsid};
export const dbCcsid = ${dbCcsid};
`;
}

function generateDoubleByteModule(ccsid, toUnicode, fromUnicode) {
  const toU = charArrayToU16Hex(toUnicode);
  const fromU = fromUnicode ? charArrayToU16Hex(fromUnicode) : '';

  return `// Generated CCSID ${ccsid} - Double-byte table
// Do not edit manually. Generated by scripts/generate-ccsid-tables.js
export const ccsid = ${ccsid};
export const type = 'double';
export const toUnicode = '${toU}';
export const fromUnicode = '${fromU}';
`;
}

function processFile(filePath) {
  const fileName = basename(filePath);
  const match = fileName.match(/^ConvTable(\d+)\.java$/);
  if (!match) return null;

  const ccsid = parseInt(match[1]);
  const source = readFileSync(filePath, 'utf-8');
  const tableType = detectTableType(source);

  if (!tableType) return null;

  if (tableType === 'mixed') {
    const subs = extractMixedSubCcsids(source);
    if (subs) {
      return {
        ccsid,
        type: 'mixed',
        content: generateMixedModule(ccsid, subs.sbCcsid, subs.dbCcsid)
      };
    }
    return null;
  }

  if (tableType === 'single') {
    const toUnicode = parseJavaUnicodeString(source, 'toUnicode_');
    const fromUnicode = parseJavaUnicodeString(source, 'fromUnicode_');

    if (!toUnicode) return null;

    return {
      ccsid,
      type: 'single',
      content: generateSingleByteModule(ccsid, toUnicode, fromUnicode)
    };
  }

  if (tableType === 'double') {
    const toUnicode = parseJavaUnicodeString(source, 'toUnicode_');
    const fromUnicode = parseJavaUnicodeString(source, 'fromUnicode_');

    if (!toUnicode) return null;

    return {
      ccsid,
      type: 'double',
      content: generateDoubleByteModule(ccsid, toUnicode, fromUnicode)
    };
  }

  return null;
}

// Main
mkdirSync(OUT_DIR, { recursive: true });

const files = readdirSync(JTOPEN_DIR)
  .filter(f => /^ConvTable\d+\.java$/.test(f))
  .sort((a, b) => {
    const na = parseInt(a.match(/\d+/)[0]);
    const nb = parseInt(b.match(/\d+/)[0]);
    return na - nb;
  });

const generated = [];
let singleCount = 0;
let doubleCount = 0;
let mixedCount = 0;
let skipped = 0;

for (const file of files) {
  const ccsid = parseInt(file.match(/\d+/)[0]);
  const isTier1or2 = TIER1.has(ccsid) || TIER2_SINGLE.has(ccsid) || TIER2_MIXED.has(ccsid);

  // Process all found tables
  const result = processFile(join(JTOPEN_DIR, file));
  if (result) {
    const outFile = join(OUT_DIR, `ccsid-${result.ccsid}.js`);
    writeFileSync(outFile, result.content);
    generated.push(result);

    if (result.type === 'single') singleCount++;
    else if (result.type === 'double') doubleCount++;
    else if (result.type === 'mixed') mixedCount++;

    const tier = TIER1.has(ccsid) ? 'T1' :
                 TIER2_MIXED.has(ccsid) ? 'T2' : 'T3+';
    console.log(`  [${tier}] CCSID ${result.ccsid} (${result.type})`);
  } else {
    skipped++;
  }
}

// Generate index file
const imports = generated.map((g, i) =>
  `import * as t${i} from './ccsid-${g.ccsid}.js';`
).join('\n');

const entries = generated.map((g, i) =>
  `  [${g.ccsid}, t${i}]`
).join(',\n');

const indexContent = `// Generated CCSID table registry
// Do not edit manually. Generated by scripts/generate-ccsid-tables.js
${imports}

export const generatedConvTables = new Map([
${entries}
]);
`;

writeFileSync(join(OUT_DIR, 'index.js'), indexContent);

console.log(`\nGenerated ${generated.length} tables:`);
console.log(`  Single-byte: ${singleCount}`);
console.log(`  Double-byte: ${doubleCount}`);
console.log(`  Mixed: ${mixedCount}`);
console.log(`  Skipped: ${skipped}`);
console.log(`\nOutput: ${OUT_DIR}`);
